EventAction::EventAction(RunAction* run,DetectorConstruction* det,PrimaryGeneratorAction* pga)
:detector(det),pga(pga),dedx(),runAct(run),printModulo(1),eventMessenger(0)
{

}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

EventAction::~EventAction()
{
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void EventAction::BeginOfEventAction(const G4Event* evt)
{
  int eventno = evt->GetEventID();
  if (eventno%100==0){
    G4cout << "### Event " << evt->GetEventID() << " start." << G4endl;
  }
  dedx.setup(runAct->runno,evt->GetEventID(),pga->GetAngle(),detector->calorMinId(),detector->calorMaxId());  
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void EventAction::EndOfEventAction(const G4Event* evt)
{
  int eventno = evt->GetEventID();
  if (eventno%100==0){
    G4cout << "### Event " << evt->GetEventID() << " Done." << G4endl;
  }
  DEDXDatabase::data.push_back(dedx);
}  

